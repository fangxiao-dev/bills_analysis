<#
"""
Manage frontend/node_modules sharing across worktrees.

Modes:
- Status: show whether current worktree uses a private directory, shared link, or is missing node_modules
- Link: replace current frontend/node_modules with a junction pointing at a source worktree
- Materialize: replace a shared junction with a private copied directory so this worktree can mutate dependencies
"""
#>

[CmdletBinding(SupportsShouldProcess = $true)]
param(
    [Parameter(Mandatory = $true)]
    [ValidateSet("Status", "Link", "Materialize")]
    [string]$Mode,

    [string]$SourceWorktree,

    [switch]$Force
)

$ErrorActionPreference = "Stop"

$repoRoot = (git rev-parse --show-toplevel).Trim()
if ([string]::IsNullOrWhiteSpace($repoRoot)) {
    throw "Cannot resolve git repo root."
}

$targetNodeModules = Join-Path $repoRoot "frontend\node_modules"

function Get-NodeModulesStatus {
    param(
        [Parameter(Mandatory = $true)]
        [string]$Path
    )

    if (-not (Test-Path -LiteralPath $Path)) {
        return [pscustomobject]@{
            Path = $Path
            State = "missing"
            LinkType = ""
            ResolvedTarget = ""
            Note = ""
        }
    }

    $item = Get-Item -LiteralPath $Path -Force
    $linkType = ""
    if ($item.PSObject.Properties.Name -contains "LinkType") {
        $linkType = [string]$item.LinkType
    }

    $resolvedTarget = Resolve-LinkTarget -Item $item
    $isLinked = $false
    if ($resolvedTarget) {
        $isLinked = $true
    } elseif (($item.Attributes -band [IO.FileAttributes]::ReparsePoint) -ne 0) {
        $isLinked = $true
    }

    return [pscustomobject]@{
        Path = $item.FullName
        State = $(if ($isLinked) { "linked" } else { "private" })
        LinkType = $linkType
        ResolvedTarget = $resolvedTarget
        Note = $(if ($isLinked -and -not $resolvedTarget) { "Linked path detected but target could not be resolved." } else { "" })
    }
}

function Resolve-LinkTarget {
    param(
        [Parameter(Mandatory = $true)]
        [System.IO.FileSystemInfo]$Item
    )

    if ($Item.PSObject.Properties.Name -contains "Target") {
        $target = $Item.Target
        if ($target -is [Array]) {
            $target = $target[0]
        }
        if (-not [string]::IsNullOrWhiteSpace([string]$target)) {
            try {
                return (Resolve-Path -LiteralPath $target).Path
            } catch {
                return [string]$target
            }
        }
    }

    try {
        $cmdLine = "fsutil reparsepoint query `"$($Item.FullName)`""
        $output = cmd /c $cmdLine 2>$null
        $printName = $output | Where-Object { $_ -match "Print Name:\s+(.*)$" } | Select-Object -First 1
        if ($printName -match "Print Name:\s+(.*)$") {
            $candidate = $Matches[1].Trim()
            if (-not [string]::IsNullOrWhiteSpace($candidate)) {
                return $candidate
            }
        }
    } catch {
        return ""
    }

    return ""
}

function Remove-NodeModulesPath {
    param(
        [Parameter(Mandatory = $true)]
        [string]$Path,

        [Parameter(Mandatory = $true)]
        [string]$CurrentState
    )

    if (-not (Test-Path -LiteralPath $Path)) {
        return
    }

    if ($CurrentState -eq "linked") {
        Remove-Item -LiteralPath $Path -Force
        return
    }

    Remove-Item -LiteralPath $Path -Recurse -Force
}

function Resolve-SourceWorktree {
    param(
        [Parameter(Mandatory = $true)]
        [string]$CurrentRoot,

        [string]$ExplicitPath
    )

    if (-not [string]::IsNullOrWhiteSpace($ExplicitPath)) {
        return (Resolve-Path -LiteralPath $ExplicitPath).Path
    }

    $preferredBranches = @("refs/heads/main", "refs/heads/dev")
    $worktreeEntries = Get-WorktreeEntries
    $candidate = $worktreeEntries |
        Where-Object {
            $_.Path -ne $CurrentRoot -and
            $preferredBranches -contains $_.Branch -and
            (Test-Path -LiteralPath (Join-Path $_.Path "frontend\node_modules"))
        } |
        Select-Object -First 1

    if (-not $candidate) {
        $candidate = $worktreeEntries |
            Where-Object {
                $_.Path -ne $CurrentRoot -and
                $_.Branch -and
                (Test-Path -LiteralPath (Join-Path $_.Path "frontend\node_modules"))
            } |
            Select-Object -First 1
    }

    if (-not $candidate) {
        throw "Unable to auto-detect a source worktree. Pass -SourceWorktree explicitly."
    }

    return $candidate.Path
}

function Get-WorktreeEntries {
    $entries = @()
    $current = @{}
    foreach ($line in (git worktree list --porcelain)) {
        if ([string]::IsNullOrWhiteSpace($line)) {
            if ($current.Path) {
                $entries += [pscustomobject]$current
            }
            $current = @{}
            continue
        }
        if ($line -like "worktree *") {
            $current.Path = $line.Substring("worktree ".Length).Trim()
            continue
        }
        if ($line -like "branch *") {
            $current.Branch = $line.Substring("branch ".Length).Trim()
        }
    }
    if ($current.Path) {
        $entries += [pscustomobject]$current
    }
    return $entries
}

switch ($Mode) {
    "Status" {
        $status = Get-NodeModulesStatus -Path $targetNodeModules
        Write-Host ("Worktree:      " + $repoRoot)
        Write-Host ("NodeModules:   " + $status.Path)
        Write-Host ("State:         " + $status.State)
        if ($status.LinkType) {
            Write-Host ("LinkType:      " + $status.LinkType)
        }
        if ($status.ResolvedTarget) {
            Write-Host ("Source:        " + $status.ResolvedTarget)
        }
        if ($status.Note) {
            Write-Host ("Note:          " + $status.Note)
        }
        break
    }

    "Link" {
        $sourceRoot = Resolve-SourceWorktree -CurrentRoot $repoRoot -ExplicitPath $SourceWorktree
        $sourceNodeModules = Join-Path $sourceRoot "frontend\node_modules"
        if (-not (Test-Path -LiteralPath $sourceNodeModules)) {
            throw "Source node_modules not found: $sourceNodeModules"
        }
        if ($sourceNodeModules -eq $targetNodeModules) {
            throw "Source and target node_modules are the same path."
        }

        $current = Get-NodeModulesStatus -Path $targetNodeModules
        if ($current.State -eq "linked" -and $current.ResolvedTarget -eq $sourceNodeModules) {
            Write-Host "frontend/node_modules already linked to the requested source."
            break
        }
        if ($current.State -eq "private" -and -not $Force) {
            throw "Current frontend/node_modules is a private directory. Re-run with -Force to replace it with a shared link."
        }

        if ($PSCmdlet.ShouldProcess($targetNodeModules, "Link frontend/node_modules to $sourceNodeModules")) {
            Remove-NodeModulesPath -Path $targetNodeModules -CurrentState $current.State
            New-Item -ItemType Junction -Path $targetNodeModules -Target $sourceNodeModules | Out-Null
            Write-Host ("Linked frontend/node_modules -> " + $sourceNodeModules)
        } else {
            Write-Host ("Would link frontend/node_modules -> " + $sourceNodeModules)
        }
        break
    }

    "Materialize" {
        $current = Get-NodeModulesStatus -Path $targetNodeModules
        if ($current.State -eq "missing") {
            throw "frontend/node_modules is missing. Nothing to materialize."
        }
        if ($current.State -eq "private") {
            Write-Host "frontend/node_modules is already a private directory."
            break
        }
        if (-not $current.ResolvedTarget) {
            throw "Cannot resolve the current shared source for frontend/node_modules."
        }
        if (-not (Test-Path -LiteralPath $current.ResolvedTarget)) {
            throw "Shared source does not exist: $($current.ResolvedTarget)"
        }

        $stagingPath = "$targetNodeModules.__materialize__"
        if (Test-Path -LiteralPath $stagingPath) {
            Remove-Item -LiteralPath $stagingPath -Recurse -Force
        }

        if ($PSCmdlet.ShouldProcess($targetNodeModules, "Materialize frontend/node_modules from $($current.ResolvedTarget)")) {
            Copy-Item -LiteralPath $current.ResolvedTarget -Destination $stagingPath -Recurse
            Remove-NodeModulesPath -Path $targetNodeModules -CurrentState $current.State
            Move-Item -LiteralPath $stagingPath -Destination $targetNodeModules
            Write-Host ("Materialized private frontend/node_modules from " + $current.ResolvedTarget)
        } else {
            Write-Host ("Would materialize private frontend/node_modules from " + $current.ResolvedTarget)
        }
        break
    }
}
